#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	unsigned int		s;
	char				*ptr;

	s = count * size;
	ptr = malloc(s);
	if (!ptr)
		return (NULL);
	ft_memset(ptr, 0, s);
	return (ptr);
}
